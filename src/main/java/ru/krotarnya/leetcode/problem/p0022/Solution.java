    public static List<String> generateParenthesis(int n) {
        return generateParenthesisPrecalculated(n);
    }

    public static List<String> generateParenthesisPrecalculated(int n) {
        return PRECALCULATED.get(n);
    }

    public static List<String> generateParenthesisRecursive(int n) {
        List<String> result = new ArrayList<>();
        char[] chars = new char[n * 2];
        generateParenthesisRecursive(chars, 0, result);

        return result;
    }

    public static void generateParenthesisRecursive(char[] chars, int i, List<String> result) {
        if (i == chars.length) {
            if (validateParenthesis(chars)) result.add(String.valueOf(chars));
            return;
        }

        chars[i] = '(';
        generateParenthesisRecursive(chars, i + 1, result);
        chars[i] = ')';
        generateParenthesisRecursive(chars, i + 1, result);
    }

    public static boolean validateParenthesis(char[] chars) {
        int sum = 0;
        for (char c : chars) {
            if (c == '(')
                sum++;
            else if (c == ')')
                sum--;
            if (sum < 0) return false;
        }
        return (sum == 0);
    }

